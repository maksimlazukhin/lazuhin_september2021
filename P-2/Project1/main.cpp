#include <iostream>


int main()
{
	double x; const double a = 0.9;
	std::cout << "x="; std::cin >> x;
	if (x > 1)
	{
		std::cout << a * log10(x) + pow(abs(x), 1 / 2);
	}
	else
	{
		std::cout << 2 * a * cos(x) + 3 * pow(x, 2);
	}
}

