#include <iostream>
#include <Cstring>
int main()
{
    system("chcp 1251");    //ввод вывод в консоль на русском языке
    std::string a;
    //   setlocale(LC_ALL, "Russian");
    std::cout << "Ведети время суток(утро,день,вечер,ночь)";
    std::cin >> a;
    if (a == "утро")
    {
        std::cout << "Доброе утро!";
    }
    else
    {
        if (a == "день")
        {
            std::cout << ("Добрый день!");
        }
        else
        {
            if (a == "вечер")
            {
                std::cout << ("Добрый вечер!");
            }
            else
            {
                if (a == "ночь")
                {
                    std::cout << "Спокойной ночи!";
                }
            }
        }
    }
}

